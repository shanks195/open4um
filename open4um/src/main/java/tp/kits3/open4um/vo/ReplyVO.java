package tp.kits3.open4um.vo;

public class ReplyVO {
	private String cmtname;
	private String datecmt;
	private String username;
	private int parent;
	private int productid;
	
	public String getCmtname() {
		return cmtname;
	}
	public void setCmtname(String cmtname) {
		this.cmtname = cmtname;
	}
	public String getDatecmt() {
		return datecmt;
	}
	public void setDatecmt(String datecmt) {
		this.datecmt = datecmt;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getParent() {
		return parent;
	}
	public void setParent(int parent) {
		this.parent = parent;
	}
	public int getProductid() {
		return productid;
	}
	public void setProductid(int productid) {
		this.productid = productid;
	}
	
	
}
